class EorO {
    public static void main(String[] args) {
        int x = 6;
        if ( x == 0 ) {
            System.out.println( "X is Neutral" );
        } else if ( x % 2 == 0 ) {
            System.out.println( "X is Even Number." );
        } else if ( x % 2 == 1 ) {
            System.out.println( "X is Odd Number.");
        }
    }
}