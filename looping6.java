class looping6 {
    public static void main(String[] args) {
        System.out.println("No. 21");
        int x = 6,i,j;
        for ( i = 1; i <= x; i ++) {
            for ( j = 1; j <= i; j++) {
                System.out.print(j + " ");
            }
        System.out.println();
        }   
    }
}